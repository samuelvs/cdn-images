import { AfterViewInit, Component, ElementRef, QueryList, signal, ViewChildren } from '@angular/core';

@Component({
  selector: 'app-root',
  standalone: true,
  templateUrl: './app.component.html',
  styleUrls: ['./app.component.scss']
})
export class AppComponent implements AfterViewInit {

  // Arrays com URLs simuladas (coloque as URLs reais)
  cloudfront = "https://d3o2c1r7qu60go.cloudfront.net/";
  azion = "https://lpsimages-2025.azioncdn.net/";

  imagens = signal<string[]>([]);

  @ViewChildren('imgs') imgs!: QueryList<ElementRef<HTMLImageElement>>;

  observer?: IntersectionObserver;

  carregarCloudfront() {
    this.imagens.set(images.map(image => this.cloudfront + image));
  }

  carregarAzion() {
    this.imagens.set(images.map(image => this.cloudfront + image));
  }



ngAfterViewInit() {
  this.observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        const img = entry.target as HTMLImageElement;
        const src = img.getAttribute('data-src');
        if (src) {
          img.src = src;
          this.observer?.unobserve(img);
        }
      }
    });
  }, {
    rootMargin: '200px 0px' // já começa a carregar um pouco antes de aparecer
  });

  // Quando as imagens renderizarem, observar cada uma
  this.imgs.changes.subscribe(() => {
    this.imgs.forEach(imgEl => this.observer?.observe(imgEl.nativeElement));
  });

  // Observar as imagens que já estão no DOM (caso já tenham)
  this.imgs.forEach(imgEl => this.observer?.observe(imgEl.nativeElement));
}
}


